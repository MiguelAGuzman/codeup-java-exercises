public class StringExercise {
    public static void main(String[] args) {

        String firstS = "We don't need no education \n" +
                "We don't need no thought control";
        System.out.println(firstS);

        String secondS = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(secondS);

        String thirdS = "In windows, the main drive is usually C:\\";
        System.out.println(thirdS);

        String fourthS = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(fourthS);
    }
}
